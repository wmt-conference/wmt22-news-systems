K uskutečnění útoků na principu „distribuované odepření služby“ (DDoS) používají hackeři neskutečný počet nabouraných a kontrolovaných zdrojů (počítačů, chytrých telefonů, tabletů).
Tato technika může být rozeseta po celém světě.
