Došlo k tomu v sobotu několik hodin poté, co Británie odsouhlasila vydání dalších 23 licencí francouzským rybářům, aby zmírnila napětí mezi těmito dvěma sousedními státy, které se posledních šest měsíců dohadovaly kvůli rybolovu.
Francie požaduje dalších 81 schválení a vydání 104 licencí, které potřebují plavidla pro provozování rybolovu v oblastech kolem britských a normandských ostrovů na základě smlouvy o Brexitu podepsané vloni.
