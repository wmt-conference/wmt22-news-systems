I přes pozitivní pověst v zahraničí De Klerk rozdělil názor v Jižní Africe a jeho smrt vyvolala smíšené reakce.
Kritici říkají, že zůstává neoddělitelný od zločinů éry apartheidu a mohl za ně být učiněný zodpovědný, kdyby žil déle.
De Klerk představoval Národní stranu, která v roce 1948 vytvořila rasovou segregaci apartheidu a rozčarování z jihoafrické nebělošské většiny.
Mimo kostel držela malá skupina protestujících cedule říkající „Spravedlnost byla popřena“ a „Spravedlnost pro oběti apartheidu“ a byli rychle odvedeni policií“.
