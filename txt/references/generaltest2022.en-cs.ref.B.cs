Test molekulární diagnostiky umí odhalit variantu Omicron za 20 minut: Zpráva
Korejští vědci vyvinuli molekulární diagnostickou technologii, která dokáže detekovat varianty Omicron.
Vývoj technologie byl v současné době dokončen a očekává se, že její uvedení na trh bude trvat delší dobu.
POSTECH 10tého oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže odhalit variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které je 26-32 mutací ve špici, která se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace na úrovni jedné nukleotidové báze, takže dokáže odhalit „skryté omikrony“, které jsou obtížně detekovatelné pomocí PCR testů.
V současné době korejské Centrum pro kontrolu a prevenci nemocí používá k detekci variant COVID-19 tři metody: analýzu celého genomu, analýzu cílové DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta ji lze detekovat současným PCR testem, ale variantu Omicron nikoli.
Nově vyvinutou technologií tentokrát není metoda sekvenování, která čte sekvence DNA nebo RNA, ale technologie molekulární diagnostiky.
Stávající technologie prověřuje pouze určité oblasti viru, ale technologie molekulární diagnostiky byla navržena tak, aby vyvolávala reakce vázající nukleové kyseliny pouze v případě existence RNA COVID-19, což umožňuje rychlé odhalení.
Podle profesora Leeho má Omicron při PCR testech silný signál pro geny N, ale slabý signál pro geny S.
V případě „skrytého omikronu“ se potvrdila pozitivita genů N i S, což ztěžuje odlišení od ostatních variant.
Technologie molekulární diagnostiky pracuje jiným mechanismem než PCR a účinně detekuje variantu Omicron.
Na rozdíl od běžné technologie, kde se obvykle zpracovává až 96 vzorků na jedno zařízení, může nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Tato technologie navíc nepotřebuje specializované vybavení, a proto lze diagnostické soupravy vyrobit jednoduše a snadno.
Vzhledem k tomu, že metoda dokáže vyvinout diagnostickou soupravu za 4 dny, očekává se rychlá reakce i v případě, že se v budoucnu objeví nová varianta nebo virus.
„Doufám, že odhalení této technologie nám pomůže vrátit se co nejdříve do normálního každodenního života,“ řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které se mohou objevit po COVID-19.
Tato technologie je v současné době před uvedením na trh.
Lze ji však použít jako pomocný prostředek v současnosti, kdy nebyl vyvinut PCR test pro Omicron.
