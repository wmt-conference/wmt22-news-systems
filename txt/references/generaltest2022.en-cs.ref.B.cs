Jižní Afrika vzdala v neděli oficiální poctu FW de Klerkoví, poslednímu prezidentovi bílé vlády, který osvobodil z vězení Nelsona Mandelu a nasměroval tuto zemi z apartheidu k demokracii.
De Klerk zemřel 11. listopadu ve věku 85 let po boji s rakovinou.
Na jeho počest byly vyhlášeny čtyři dny státního smutku.
Sloužil jako prezident od roku 1989 do roku 1994 a je nejvíce spojován s vedením přechodu Jižní Afriky od bílé většinové vlády k prvním vícerasovým volbám roce 1994.
