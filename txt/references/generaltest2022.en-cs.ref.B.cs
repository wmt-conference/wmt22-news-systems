Všechno,“ řekl.
„A zřejmě to má nějaký dopad i tady.“
V šesti různých státech bylo hlášeno nejméně 30 tornád, která zpustošila rozsáhlá území a očekává se, že bouře zabila více než 100 lidí.
Prezident uvedl, že nezná kompletní rozsah vlivu globálního oteplování na smrtící bouře, které označil za jednu z „největších epidemií tornád v historii.“
