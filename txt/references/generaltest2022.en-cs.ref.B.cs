Mandela se pak stal prvním jihoafrickým černým prezidentem poté, co jeho strana afrického národního kongresu vyhrála v roce 1994 volby.
Prezident Cyril Ramaphosa navštívil protestanský kostel v Kapském Městě Groote Kerk - jeden z nejstarších jihoafrických kostelů - v neděli ráno, aby pronesl oslavnou řeč na počest De Klerka.
„Často byl nepochopen kvůli jeho nadměrné korektnosti“, řekla vdova po Klerkovi Elita Georgiadisová kolem 200 návštěvníkům.
