Autorka „Interview s upírem“ Anne Riceová zemřela ve věku 80 let
Christopher Rice uvedl, že zemřela v důsledku komplikací způsobených mrtvicí.
Největšího úspěchu dosáhla Riceová svým prvním románem „Interview s upírem“, který vyšel v roce 1976 a představil postavu upíra Lestata, jenž se stal ústřední postavou třináctidílné série Kronika, jejíž poslední díl vyšel v roce 2018.
„Měla jsem představu Lestata jako muže činu, muže, který dokáže věci, které já nedokážu,“ řekla Riceová na přednášce na Southern Illinois University v roce 2010.
„Interview s upírem“ se v roce 1994 dočkalo úspěšného celovečerního filmu a přispělo k oživení zájmu o upírský žánr, který pokračoval televizním seriálem „Deníky upíra“ a filmovou sérií „Stmívání“.
