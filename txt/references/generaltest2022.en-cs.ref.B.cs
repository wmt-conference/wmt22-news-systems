Joe Biden po smrtících tornádech lamentuje nad neschopností zastavit globální oteplování
Prezident Joe Biden v sobotu litoval, že se světu nedaří zastavit globální oteplování, poté co se vyjádřil k smrtícím tornádům, která se prohnala několika státy.
