Molekulární diagnostický test může detekovat variantu Omicron za 20 minut: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicronu.
Vývoj technologie byl v současné době dokončen a očekává se, že komercializace bude nějakou dobu trvat.
POSTECH oznámil 10., že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací v hrotu, který se používá k infekci buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže může detekovat "Stealth Omicron", které je obtížné detekovat pomocí PCR testů.
V současné době korejská centra pro kontrolu a prevenci nemocí používají tři metody k detekci variant COVID-19: celogenomovou analýzu, analýzu cílové DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta může být detekována aktuálním PCR testem, ale Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenační metoda, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobila reakce vázající nukleové kyseliny pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě "Stealth Omicron" byly geny N i S potvrzeny pozitivní, což ztěžuje odlišení od ostatních variant.
Molekulární diagnostická technologie pracuje v různých mechanismech než PCR a efektivně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, a proto může jednoduše a snadno vyrábět diagnostické soupravy.
Vzhledem k tomu, že metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude rychle reagovat, i když se v budoucnu objeví nová varianta nebo virus.
"Doufám, že odhalení této technologie nám pomůže vrátit se k normálnímu každodennímu životu co nejdříve," řekl profesor Lee.
Pokusíme se rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocná látka v současných situacích, kdy nebyl vyvinut test PCR pro Omicron.
Profesor Lee řekl: "Myslím, že tato technologie bude blízko komercializaci ve druhé polovině příštího roku po klinických studiích.
Důvodem, proč tuto technologii zveřejňuji, je sdílet ji s ostatními, abychom vyvinuli lepší technologie pro překonání COVID-19 a umožnili zaostalým zemím také analyzovat varianty COVID-19."
