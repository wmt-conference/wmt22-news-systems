"Rozhovor s upírem" autor Anne Rice umírá ve věku 80 let
Zemřela kvůli komplikacím způsobeným mrtvicí, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román "Rozhovor s upírem", který vyšel v roce 1976 a představil postavu upíra Lestata, který by byl ústřední postavou v sérii Chronicles o 13 knihách, z nichž poslední byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži činu, muži, který může dělat věci, které já nemůžu dělat," řekl Rice v rozhovoru na Southern Illinois University v roce 2010.
"Interview with the Vampire" byl v roce 1994 zfilmován jako úspěšný celovečerní film, který pomohl znovu vzbudit zájem o upírský žánr, který pokračoval televizním seriálem "The Vampire Diaries" a filmovou sérií "Twilight".
Ačkoli žila většinu svého života v Kalifornii, Rice pocházela z New Orleans a podle své biografie na webových stránkách tam umístila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans, s veřejným památníkem plánovaným na příští rok, řekl.
