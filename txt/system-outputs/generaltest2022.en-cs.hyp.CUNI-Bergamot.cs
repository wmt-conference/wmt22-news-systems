Joe Biden lamentuje nad neschopností zastavit globální oteplování po smrtících tornádech
Prezident Joe Biden si v sobotu po projevu o smrtících tornádech, která se přehnala několika státy, posteskl nad neschopností světa zastavit globální oteplování.
„Všichni víme, že všechno je intenzivnější, když se klima otepluje.
Všechno,“ řekl.
"A zjevně to má nějaký dopad i tady."
V šesti různých státech bylo hlášeno nejméně 30 tornád, která způsobila rozsáhlou devastaci, a očekává se, že bouře usmrtila přes 100 lidí.
Prezident uvedl, že nezná plný rozsah příspěvku globálního oteplování ke smrtícím bouřím, které označil za jednu z „největších epidemií tornád v dějinách“.
Řekl, že přiměje Agenturu pro ochranu životního prostředí, aby to vyšetřila.
"Jediné, co vím, je, že intenzita počasí na celé planetě má nějaký dopad v důsledku oteplování planety," řekl Biden.
Prezident pochválil reportéra, který se ho ptal na změnu klimatu.
„Jako obvykle se vždycky ptáš na tu nejlepší otázku,“ řekl s kyselým uchechtnutím.
"Jak to vyřídíme?" pokračoval.
„Částečně připouští, že pravděpodobnost menšího počtu katastrof způsobených počasím, bez pokračujícího pohybu v řešení globálního oteplování, prostě nenastane.“
Biden uvedl, že je šokován rekordními lesními požáry v zemi během roku 2021, a vyjádřil znepokojení nad tím, že globální oteplování je významným příspěvkem.
"Takže musíme jednat," řekl.
Biden řekl, že prvním krokem je záchrana životů a péče o rodiny, které byly postiženy bouřemi.
„To ti slibuji.
Cokoli je potřeba.
Cokoli je potřeba, federální vláda to dodá," řekl Biden.
