Molekulární diagnostika test dokáže odhalit Omicron variantu za 20 minut: Zpráva
Korejští výzkumníci vyvinuli molekulární diagnostickou technologii, která dokáže detekovat varianty Omicronu.
Vývoj technologie byl v současné době dokončen a očekává se, že komercializace bude nějakou dobu trvat.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní na internetu.
Omicron je varianta, v níž se vyskytuje 26-32 mutací v hrotu, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace v jednonukleotidové bázi, takže dokáže detekovat „Stealth Omicron“, které je obtížné odhalit pomocí PCR testů.
Korejské středisko pro kontrolu a prevenci nemocí v současné době používá tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace jako je bodový protein) a PCR test.
V případě varianty Delta ji lze zjistit současným PCR testem, ale Omicron nikoli.
Nově vyvinutá technologie tentokrát není sekvenční metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostickou technologií.
Stávající technologie kontroluje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby vyvolávala reakce vazby nukleových kyselin pouze tehdy, když existuje COVID-19 RNA, což umožňuje rychlou detekci.
Podle profesora Leeho má Omicron silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě „Stealth Omicron“ se geny N i S potvrdily jako pozitivní, takže bylo obtížné je odlišit od ostatních variant.
Molekulární diagnostická technologie pracuje v odlišných mechanismech od PCR a efektivně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obvykle zpracovává až 96 vzorků na jedno zařízení, jich nová technologie dokáže zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Navíc tato technologie nepotřebuje specializované vybavení, takže dokáže jednoduše a snadno vyrobit diagnostické soupravy.
Protože metoda dokáže vyvinout diagnostickou soupravu za 4 dny, očekává se, že bude rychle reagovat i v případě, že se v budoucnu objeví nová varianta nebo virus.
„Doufám, že odhalení této technologie nám pomůže vrátit se co nejdříve do běžného každodenního života,“ řekl profesor Lee.
Pokusíme se rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Lze ho však použít jako pomocnou látku v současných situacích, kdy nebyl vyvinut PCR test pro Omicron.
Profesor Lee řekl: „Myslím, že tato technologie bude po klinických zkouškách ve druhé polovině příštího roku blízko komercializaci.
Důvodem, proč tuto technologii zveřejňuji, je sdílet ji s ostatními, abychom vyvinuli lepší technologie pro překonání COVID-19 a umožnili méně rozvinutým zemím také analyzovat varianty COVID-19.“
