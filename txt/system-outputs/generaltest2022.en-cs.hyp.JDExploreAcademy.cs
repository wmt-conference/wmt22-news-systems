Molekulární diagnostický test dokáže detekovat variantu omikronu za 20 minut: zpráva
Korejští výzkumníci vyvinuli molekulární diagnostickou technologii, která dokáže detekovat varianty Omicronu.
V současné době byl dokončen technologický vývoj a očekává se, že bude trvat nějaký čas, než bude k dispozici komercializace.
POSTECH dne 10. oznámila, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které je 26-32 mutací v hrotu, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže může detekovat "Stealth Omicron", které jsou obtížně detekovatelné pomocí testů PCR.
V současné době používá Korejské centrum pro kontrolu a prevenci nemocí tři metody k detekci variant COVID-19: analýzu celého genomu, analýzu cílové DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta může být detekována současným PCR testem, ale Omicron ne.
Nově vyvinutá technologie tentokrát není sekvenační metoda, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie detekuje pouze specifické oblasti viru, ale technologie molekulární diagnostiky byla navržena tak, aby vyvolala reakci vazby nukleových kyselin pouze v případě, že existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee má Omicron silný signál pro geny N v testech PCR, ale má slabý signál pro geny S.
V případě "Stealth Omicron" byly geny N i S potvrzeny jako pozitivní, což ztěžuje odlišení od jiných variant.
Molekulární diagnostická technologie pracuje na jiných mechanismech než PCR a detekuje variantu Omicronu efektivně.
Na rozdíl od konvenční technologie, která obvykle zpracovává až 96 vzorků na jedno zařízení, dokáže nová technologie zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Tato technologie navíc nepotřebuje specializované vybavení, takže diagnostické soupravy mohou být vyrobeny jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude rychle reagovat, i když se v budoucnu objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže vrátit se co nejdříve k normálnímu každodennímu životu," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které se mohou objevit po COVID-19.
Tato technologie je v současné době před komercializací.
Může však být použit jako pomocný v současných situacích, kdy nebyl vyvinut PCR test pro Omicron.
Profesor Lee řekl: "Myslím, že tato technologie bude blízko komercializaci v druhé polovině příštího roku po klinických testech.
Důvod, proč tuto technologii zveřejňuji, je sdílet ji s ostatními, abychom vyvinuli lepší technologie pro překonání COVID-19 a umožnili zaostalým zemím také analyzovat varianty COVID-19."
