Ve věku 80 let zemřela autorka románu Interview s upírem Anne Riceová
Podle Christophera Rice zemřela na komplikace po mozkové mrtvici.
Největším úspěchem Riceové byl její první román Interview s upírem, který vyšel v roce 1976 a představil postavu upíra Lestata, který se stal ústřední postavou třináctidílné série Kroniky, z nichž poslední vyšla v roce 2018.
"Představoval jsem si Lestata jako muže činu, muže, který dokáže věci, které jsem já nedokázal," řekl Rice v roce 2010 v přednášce na Southern Illinois University.
"Rozhovor s upírem" byl v roce 1994 úspěšně zfilmován a pomohl znovu zažehnout zájem o upírský žánr, který pokračoval televizním seriálem "Upíří deníky" a filmovou sérií "Twilight".
Ačkoli žila většinu svého života v Kalifornii, Riceová pocházela z New Orleans a podle biografie na její webové stránce se mnoho jejích příběhů odehrávalo tam.
Syn Riceové, Christopher Rice, řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans, veřejná památka je plánována na příští rok, řekl.
Děkuji, že jste si udělali čas na rozhovor se mnou dnes a doufám, že se mi podařilo vyřešit váš dotaz, pokud by vám nevadilo ohodnotit náš dnešní chat na základě mých dovedností v oblasti služeb zákazníkům, opravdu bych to ocenil, tlačítko pro hodnocení najdete na tomto chatu.
Doufám, že budete mít skvělý den a prosím, vraťte se k nám, pokud budete potřebovat další pomoc.
