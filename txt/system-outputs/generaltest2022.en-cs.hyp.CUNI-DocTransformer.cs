„Rozhovor s upírem“ Anne Riceová zemřela v osmdesáti letech
Zemřela kvůli komplikacím způsobeným mrtvicí, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román „Rozhovor s upírem“, který vyšel v roce 1976 a představil postavu upíra Lestata, který bude ústřední postavou v sérii Kroniky o třinácti knihách, z nichž poslední vyšla v roce 2018.
„Měla jsem představu o Lestatovi jako muži činu, muži, který dokáže věci, které já neumím,“ řekla Riceová v roce 2010 v přednášce na Southern Illinois University.
„Rozhovor s upírem“ byl natočen v roce 1994 jako úspěšný celovečerní film, který pomohl znovu oživit zájem o upíří žánr, který pokračoval televizním seriálem „Upíří deníky“ a filmovou sérií „Stmívání“.
Ačkoli většinu svého života prožila v Kalifornii, Riceová byla rodačkou z New Orleans a podle svého životopisu na webových stránkách tam zasadila mnoho svých příběhů.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleans, s veřejným památníkem plánovaným na příští rok, řekl.
