Test molekulární diagnostiky dokáže detekovat variantu Omicron za 20 minut: zpráva
Korejští výzkumníci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicronu.
Vývoj technologie byl v současné době dokončen a očekává se, že komercializace nějakou dobu potrvá.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul technologii molekulární diagnostiky, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní na internetu.
Omicron je varianta, ve které se 26-32 mutací ve špičce, která se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu dokáže technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže dokáže detekovat „Stealth Omicron“, které jsou obtížně detekovatelné pomocí PCR testů.
V současné době Korejská centra pro kontrolu a prevenci nemocí používají tři metody detekce variant COVID-19: celogenomovou analýzu, analýzu cílové DNA (mutací, jako je protein špičky) a PCR test.
V případě varianty Delta lze detekovat pomocí současného PCR testu, ale Omicron nikoliv.
Nově vyvinutá technologie tentokrát není sekvenční metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostickou technologií.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby vyvolávala reakce na vazbu nukleových kyselin pouze v případě, že existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Leeho má Omicron silný signál pro N geny v PCR testech, ale slabý signál pro S geny.
V případě „Stealth Omicronu“ byly potvrzeny pozitivní N i S geny, což znesnadňuje odlišení od ostatních variant.
Technologie molekulární diagnostiky pracuje v jiných mechanismech než PCR a detekuje variantu Omicronu efektivně.
Na rozdíl od konvenční technologie, která zpravidla zpracovává až 96 vzorků na jedno zařízení, dokáže nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Tato technologie navíc nepotřebuje specializované vybavení, takže může jednoduše a snadno vyrobit diagnostické soupravy.
Vzhledem k tomu, že metoda dokáže vyvinout diagnostický kit za 4 dny, očekává se, že bude reagovat rychle i v případě, že se v budoucnu objeví nová varianta nebo virus.
„Doufám, že odhalení této technologie nám pomůže vrátit se co nejdříve do běžného každodenního života,“ řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Nicméně může být použita jako pomocný prostředek v současných situacích, kdy nebyl vyvinut PCR test pro Omicron.
Profesor Lee řekl: „Myslím, že tato technologie bude blízko komercializaci v druhé polovině příštího roku po klinických zkouškách.
Důvodem, proč zveřejňuji tuto technologii, je její sdílení s ostatními, aby se vyvinuly lepší technologie pro překonání COVID-19 a aby i zaostalé země mohly analyzovat varianty COVID-19.“
