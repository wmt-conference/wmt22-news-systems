Joe Biden Laments nedokázal zastavit globální oteplování po smrtících tornádech
Prezident Joe Biden si v sobotu posteskl, že svět nedokázal zastavit globální oteplování poté, co se vyjádřil ke smrtícím tornádům, která se prohnala několika státy.
„Všichni víme, že všechno je intenzivnější, když se otepluje klima.
Všechno,“ řekl.
„A samozřejmě to má nějaký dopad i tady.“
V šesti různých státech bylo hlášeno nejméně 30 tornád, která způsobila rozsáhlou devastaci, a očekává se, že v důsledku bouře zahynulo přes 100 lidí.
Prezident řekl, že nezná plný rozsah podílu globálního oteplování na smrtících bouřích, které označil za jednu z „největších ohnisek tornád v historii“.
Řekl, že požádá Agenturu pro ochranu životního prostředí, aby to prošetřila.
„Jediné, co vím, je, že intenzita počasí má nějaký dopad v důsledku oteplování planety,“ řekl Biden.
Prezident pochválil reportéra, který se ho ptal na změnu klimatu.
„Jako obvykle se vždycky ptáte na tu nejlepší otázku,“ řekl s kyselým úsměvem.
„Jak to budeme řešit?“ pokračoval.
„Částečně uznávám, že pravděpodobnost menšího počtu meteorologických katastrof, bez pokračujícího postupu v řešení globálního oteplování, se prostě nestane.“
Biden řekl, že byl šokován rekordními požáry v zemi v roce 2021, a vyjádřil znepokojení nad tím, že globální oteplování je hlavním přínosem.
„Takže musíme jednat,“ řekl.
Biden řekl, že prvním krokem je záchrana životů a péče o rodiny, které byly zasaženy bouřemi.
„To vám slibuji.
Cokoliv bude potřeba.
Cokoliv bude potřeba, federální vláda to dodá,“ řekl Biden.
Řekl, že bude pokračovat v pečlivém monitorování obnovy bouře a udělá vše, co bude potřeba pro federální vládu.
„Chci, aby to lidé ve všech těchto státech věděli.
Dostaneme se z toho.
Dostaneme se z toho společně a federální vláda z toho jen tak neodejde,“ řekl.
„Tohle je jedna z těch chvil, kdy nejsme demokrati ani republikáni.“
Prezident řekl, že navštíví oblasti postižené bouří, až bude jasné, že se nebude plést do cesty místním záchranným akcím.
„Mám v plánu tam jít,“ řekl.
