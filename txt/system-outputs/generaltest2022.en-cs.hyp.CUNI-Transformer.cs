Joe Biden naříká, že se mu nepodařilo zastavit globální oteplování po smrtících tornádech
Prezident Joe Biden si v sobotu posteskl nad tím, že svět nedokázal zastavit globální oteplování poté, co se vyjádřil ke smrtícím tornádům, která se přehnala několika státy.
„Všichni víme, že všechno je intenzivnější, když se klima otepluje.
Všechno,“ řekl.
„A tady to má samozřejmě nějaký dopad.“
V šesti různých státech bylo hlášeno nejméně 30 tornád, která způsobila rozsáhlé škody, a očekává se, že v důsledku bouře zahynulo přes 100 lidí.
Prezident uvedl, že nezná plný rozsah podílu globálního oteplování na smrtících bouřích, které označil za jedno z „největších tornád v historii“.
Řekl, že přiměje Úřad pro ochranu životního prostředí, aby to vyšetřil.
„Jediné, co vím, je, že intenzita počasí na celé palubě má nějaký dopad v důsledku oteplování planety,“ řekl Biden.
Novináře, který se ho ptal na změnu klimatu, prezident pochválil.
„Jako obvykle se vždycky ptáš na tu nejlepší otázku,“ řekl a trpce se uchechtl.
„Jak se s tím vypořádáme?“ pokračoval.
„Částečně je to přiznání, že pravděpodobnost menšího počtu katastrof způsobených počasím, bez pokračujícího vývoje v boji s globálním oteplováním, k tomu prostě nedojde.“
Biden řekl, že byl šokován rekordními požáry v zemi během roku 2021, a vyjádřil znepokojení nad tím, že globální oteplování je významným příspěvkem.
„Takže musíme jednat,“ řekl.
Biden řekl, že prvním krokem bylo zachránit životy a postarat se o rodiny, které byly zasaženy bouří.
„To ti slibuju.
Cokoliv bude potřeba.
Cokoliv bude potřeba, federální vláda to dodá,“ řekl Biden.
Prohlásil, že bude pokračovat v pečlivém sledování obnovy bouře a udělá vše, co bude federální vláda potřebovat.
„Chci, aby to věděli lidé ve všech těchto státech.
Dostaneme se z toho.
Společně to zvládneme a federální vláda z toho jen tak neodejde,“ řekl.
„Tohle je jedna z těch chvil, kdy nejsme ani demokraté, ani republikáni.“
Prezident řekl, že navštíví oblasti postižené bouří, až bude jasné, že nebude překážet místním záchranným pracím.
„Mám v plánu tam jít,“ řekl.
