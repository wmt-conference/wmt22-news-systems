Molekulární diagnostika test může odhalit Omicron varianta za 20 minut: Zpráva
Korejští výzkumníci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat Omicron.
Technologický vývoj byl v současné době dokončen a očekává se, že bude nějakou dobu trvat, než bude komercializace dokončena.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicronu za pouhých 20-30 minut a výsledky zveřejní na internetu.
Omicron je varianta, ve které se vyskytuje 26-32 mutací v hrotu, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu, technologie molekulární diagnostiky dokáže rozlišit mutace na bázi jednoho nukleotidu, takže může detekovat „Stealth Omicron“, které jsou obtížně zjistitelné pomocí PCR testů.
V současné době používají Korejská centra pro kontrolu a prevenci nemocí tři metody k detekci variant COVID-19: celogenomová analýza, analýza cílové DNA (mutací, jako je např. bodový protein) a PCR test.
V případě varianty Delta ji lze zjistit aktuálním PCR testem, ale Omicron ne.
Nově vyvinutá technologie tentokrát není sekvenovací metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostickou technologií.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby vyvolala reakci vázající nukleové kyseliny pouze v případě, že existuje RNA COVID-19, která umožňuje rychlou detekci.
Podle profesora Lee, Omicron má silný signál pro N geny v PCR testech, ale má slabý signál pro S geny.
V případě „Stealth Omicron“, oba N a S geny potvrdily pozitivní, takže je obtížné odlišit od ostatních variant.
Technologie molekulární diagnostiky pracuje v jiných mechanismech než PCR a účinně detekuje Omicron variantu.
Na rozdíl od konvenční technologie, která obvykle zpracovává až 96 vzorků na zařízení, nová technologie dokáže zpracovat více než 125 vzorků za 30 minut (více než 250 vzorků za hodinu).
Tato technologie navíc nepotřebuje specializované vybavení, takže dokáže jednoduše a jednoduše vyrobit diagnostické soupravy.
Vzhledem k tomu, že metoda dokáže vytvořit diagnostickou soupravu za 4 dny, očekává se, že bude reagovat rychle i v případě, že se v budoucnu objeví nová varianta nebo virus.
„Doufám, že odhalení této technologie nám pomůže vrátit se co nejdříve do normálního každodenního života,“ řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty, které mohou vyjít po COVID-19.
Tato technologie je v současné době před komercializací.
Lze jej však použít jako pomocný v současných situacích, kdy PCR test pro Omicron nebyl vyvinut.
Profesor Lee řekl: „Myslím, že tato technologie bude blízko komercializaci v druhé polovině příštího roku po klinických testech.
Důvodem, proč tuto technologii uveřejňuji, je její sdílení s ostatními za účelem vývoje lepších technologií pro překonání COVID-19 a umožnění nedostatečně rozvinutým zemím analyzovat i varianty COVID-19.“
