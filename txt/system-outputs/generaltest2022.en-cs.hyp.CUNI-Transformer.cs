Autorka „Rozhovoru s upírem“ Anne Rice zemřela ve věku 80 let
Zemřela na komplikace způsobené mrtvicí, řekl Christopher Rice.
Největším úspěchem Riceové byl její první román Interview s upírem, který vyšel v roce 1976 a představil postavu upíra Lestata, který bude ústřední postavou v sérii 13 knih Chronicles, z nichž poslední vyšla v roce 2018.
„Měl jsem představu, že Lestat je muž činu, muž, který dokáže věci, které já nedokážu,“ řekl Rice v roce 2010 v přednášce na Southern Illinois University.
V roce 1994 byl natočen úspěšný celovečerní film Interview s upírem, který přispěl k oživení zájmu o upíří žánr, který pokračoval televizním seriálem Upíří deníky a filmovou sérií Stmívání.
Přestože většinu svého života prožila v Kalifornii, Riceová se narodila v New Orleansu a podle svého životopisu tam napsala mnoho svých povídek.
Riceův syn Christopher Rice řekl, že byl u matčina lůžka, když zemřela.
Anne Riceová bude pohřbena při soukromém obřadu v New Orleansu, veřejný památník je plánován na příští rok, řekl.
