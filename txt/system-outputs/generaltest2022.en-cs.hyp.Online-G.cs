Hrozba byla učiněna v sobotu několik hodin poté, co Británie souhlasila s vydáním dalších 23 licencí francouzským rybářům, aby zmírnila napětí mezi dvěma sousedy,kteří byli v rybářských řadách za posledních šest měsíců.
Francie žádá o 81 dalších povolení k dosažení 104 licencí potřebných pro své lodě k provozu ve vodách britských a ostrovních ostrovů podle Dohody o Brexitu podepsané loni.
Evropská unie stanovila 10. prosince lhůtu pro to, aby Londýn udělil Licence francouzským rybářským lodím v rámci Brexitu, přičemž Paříž hrozí evropským právním jednáním v případě, že nedojde k průlomu.
Zdůraznil, že Francie má nárok na dalších 80 britských licencí, skupina zastupující rybáře v klíčovém přístavu Boulogne-sur-Mer a další podél severního pobřeží pohrozila v sobotu pozdě zahájit protesty.
"Protesty by se měly očekávat ... protesty, které se zaměří na britský dovoz, " uvedla v prohlášení místní skupina CRPMEM pro Region Hauts-de-France.
Skupina uvedla, že její členové byli "podrážděni" zprávou pouze o 23 nových licencích a cítili se" zrazeni " Evropskou komisí, která by mohla v této záležitosti zahájit právní kroky proti Británii.
CRPMEM uvedl, že protesty budou " v souladu s blokádami přístavů v Bretani, Normandii a na severu Francie, které se konaly 26.listopadu."
V ten den francouzské rybářské lodě krátce zablokovaly trajekty a další lodě v přístavech Calais, Saint-Malo a Ouistreham, zatímco vozidla byla také vyslána, aby narušila provoz a snažila se využít železniční spojení s tunelem pod Lamanšským průlivem.
