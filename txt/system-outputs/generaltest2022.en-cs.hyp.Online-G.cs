Test Molekulární Diagnostiky Dokáže Detekovat Variantu Omicronu Za 20 Minut: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicronu.
Vývoj technologie byl v současné době dokončen a očekává se, že bude nějakou dobu trvat na komercializaci.
POSTECH 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wokem z Katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací v hrotu, který se používá k infikování buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže může detekovat "Stealth Omicron", které je obtížné detekovat pomocí testů PCR.
V současné době Korejská Centra pro kontrolu a prevenci nemocí používají tři metody k detekci variant COVID-19: analýza celého genomu, analýza cílové DNA (mutace, jako je Spike protein) a test PCR.
V případě varianty Delta může být detekována současným PCR testem, ale Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenační metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostickou technologií.
Stávající technologie promítá pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce vázající nukleové kyseliny pouze v případě, že existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Leeho má Omicron silný signál pro n geny v PCR testech, ale má slabý signál pro s geny.
V případě "Stealth Omicron", oba geny N A S potvrdily pozitivní, což ztěžuje odlišení od ostatních variant.
Molekulární diagnostická technologie pracuje v různých mechanismech od PCR a účinně detekuje variantu Omicronu.
Na rozdíl od konvenční technologie, která obvykle zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje speciální vybavení, a tak může jednoduše a snadno vytvářet diagnostické soupravy.
Protože metoda může vyvinout diagnostickou sadu za 4 dny, očekává se, že bude rychle reagovat, i když se v budoucnu objeví nová varianta nebo virus.
"Doufám, že zveřejnění této technologie nám pomůže vrátit se do normálního každodenního života co nejdříve," řekl profesor Lee.
Budeme se snažit rychle diagnostikovat a reagovat na nové varianty,které mohou vyjít po COVIDU-19.
Tato technologie je v současné době před komercializací.
Může se však použít jako pomocná látka v současných situacích, kdy nebyl vyvinut PCR test na Omicron.
