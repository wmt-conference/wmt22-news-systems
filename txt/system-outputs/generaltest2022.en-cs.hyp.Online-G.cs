Riceovým největším úspěchem byl její první román "rozhovor s upírem", který vyšel v roce 1976 a představil postavu upíra Lestata, který by byl ústřední postavou v sérii 13 knih Chronicles, z nichž poslední byla vydána v roce 2018.
"Měl jsem představu o Lestatovi jako o muži činu, muži, který mohl dělat věci, které jsem nemohl udělat," řekl Rice v rozhovoru na Southern Illinois University v roce 2010.
"Rozhovor s upírem" byl natočen v roce 1994 do úspěšného celovečerního filmu, který pomohl znovu vzbudit zájem o upíří žánr, který pokračoval televizním seriálem "The Vampire Diaries " a filmovou sérií" Twilight".
