Test molekulární diagnostiky dokáže detekovat variantu Omicron za 20 minut: Zpráva
Korejští vědci vyvinuli technologii molekulární diagnostiky, která dokáže detekovat varianty Omicron.
Vývoj technologií byl v současné době dokončen, a očekává se, že bude nějakou dobu trvat na komercializaci.
Postech 10. oznámil, že výzkumný tým vedený profesorem Lee Jung-wookem z Katedry chemického inženýrství vyvinul molekulární diagnostickou technologii, která dokáže detekovat variantu Omicron za pouhých 20-30 minut a výsledky zveřejní online.
Omicron je varianta, ve které 26-32 mutací v hrotu, který se používá k infekci buněk virem COVID-19.
Podle výzkumného týmu může technologie molekulární diagnostiky rozlišit mutace na jednonukleotidové bázi, takže dokáže detekovat „Stealth Omicron“, které je obtížné detekovat pomocí PCR testů.
Korejská centra pro kontrolu a prevenci nemocí v současné době používají tři metody k detekci variant COVID-19: analýza celého genomu, analýza cílové DNA (mutace, jako je spike protein) a PCR test.
V případě varianty Delta může být detekována současným testem PCR, ale Omicron nemůže.
Nově vyvinutá technologie tentokrát není sekvenční metodou, která čte sekvence DNA nebo RNA, ale molekulární diagnostická technologie.
Stávající technologie prověřuje pouze specifické oblasti viru, ale molekulární diagnostická technologie byla navržena tak, aby způsobovala reakce vázající nukleové kyseliny pouze tehdy, když existuje RNA COVID-19, což umožňuje rychlou detekci.
Podle profesora Lee, Omicron má silný signál pro N geny v PCR testech, ale má slabý signál pro geny S.
V případě „Stealth Omicron, „Geny N i S potvrdily pozitivní, což ztěžuje rozlišení od ostatních variant.
Technologie molekulární diagnostiky funguje v různých mechanismech než PCR a efektivně detekuje variantu Omicron.
Na rozdíl od konvenční technologie, která obecně zpracovává až 96 vzorků na zařízení, může nová technologie zpracovat více než 125 za 30 minut (více než 250 vzorků za hodinu).
Kromě toho tato technologie nepotřebuje specializované vybavení, a tak může dělat diagnostické soupravy jednoduše a snadno.
Vzhledem k tomu, že metoda může vyvinout diagnostickou soupravu za 4 dny, očekává se, že rychle reaguje, i když se v budoucnu objeví nová varianta nebo virus.
„Doufám, že zveřejnění této technologie nám pomůže co nejdříve se vrátit do normálního každodenního života,“ řekl profesor Lee.
