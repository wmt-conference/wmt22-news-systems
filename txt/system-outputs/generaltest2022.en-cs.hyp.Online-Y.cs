Hrozba byla učiněna v sobotu několik hodin poté, co Británie souhlasila s vydáním dalšího 23 licence francouzským rybářům ke zmírnění napětí mezi dvěma sousedy, kteří byli posledních šest měsíců v rybářském řádku.
Francie usiluje o 81 dalších schválení, aby dosáhla 104 licencí potřebných pro své lodě k provozu ve vodách Britů a Normanských ostrovů na základě dohody o Brexitu podepsané v loňském roce.
Evropská unie stanovila prosinec 10 lhůta pro udělení licencí francouzským rybářským lodím v rámci Brexitu, přičemž Paříž ohrožuje evropské právní kroky v případě, že nedojde k průlomu.
Zdůraznila, že Francie má nárok na přibližně 80 dalších licencí Spojeného království, skupina zastupující rybáře v klíčovém přístavu Boulogne-sur-Mer a další podél severního pobřeží hrozila pozdě v sobotu zahájením protestů.
„Protesty by se měly očekávat... protesty, které se zaměří na britský dovoz,“ uvedla ve svém prohlášení místní skupina rybářského průmyslu CRPMEM pro region Hauts-de-France.
Skupina uvedla, že její členové byli „rozzuřeni“ pouze zprávami 23 nové licence a cítila se „zrazena“ Evropskou komisí, což by mohlo v této otázce zahájit právní kroky proti Británii.
CRPMEM uvedl, že protesty budou „v souladu s blokádami přístavů v Bretani, Normandii a na severu Francie, které se konaly 26. listopadu.“
V ten den, Francouzské rybářské lodě krátce zablokovaly trajekty a další lodě v přístavech Calais, Saint-Malo a Ouistreham, zatímco vozidla byla také odeslána, aby narušila provoz a snažila se využít železniční spojení tunelu pod Lamanšským průlivem.
